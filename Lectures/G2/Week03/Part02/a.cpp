#include <iostream>

using namespace std;

int main() {

    // relational operators:  >, <, ==, !=, >=, <=

    cout << (1 < 2) << endl;
    cout << (1.5 > 2.1) << endl;
    cout << (1.5 >= 2) << endl;
    cout << ('A' <= 'B') << endl; // 65 <= 66
    cout << (true == true) << endl; // 1 == 1
    cout << (true != false) << endl; // 1 != 0

    return 0;
}