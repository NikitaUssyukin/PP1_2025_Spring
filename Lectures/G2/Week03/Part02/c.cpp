#include <iostream>

using namespace std;

int main() {

    // logical operators:  
    /*
    && - logical AND
    || - logical OR
    !  - logical NOT
    */

    cout << (false && false) << endl;
    cout << (true && false) << endl;
    cout << (false && true) << endl;
    cout << (true && true) << endl;
    
    return 0;
}