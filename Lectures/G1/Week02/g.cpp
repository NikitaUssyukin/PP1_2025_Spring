#include <iostream>

using namespace std;

int main() {

    // Operators, logical

    /*
    ||  logical OR
    &&  logical AND
    !   logical NOT
    */

    bool a = true;
    bool b = false;

    cout << "a || b: " << (a || b) << endl;
    cout << "a && b: " << (a && b) << endl;
    cout << "!a: " << !a << endl;
    cout << "!b: " << !b << endl;

    return 0;
}