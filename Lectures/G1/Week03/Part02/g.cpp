#include <iostream>

using namespace std;

int main() {

    // Logical operators
    /*
    && - logical and
    || - logical or
    !  - logical not
    */

    // ||
    cout << (false || false) << endl;
    cout << (true || false) << endl;
    cout << (false || true) << endl;
    cout << (true || true) << endl;

    return 0;
}