#include <iostream>

using namespace std;

int main() {

    // Relational operators
    /*
    > 
    < 
    == 
    != 
    >= 
    <=
    */

    int a, b;
    cin >> a >> b;

    cout << "(a > b): " << (a > b) << endl;
    cout << "(a < b): " << (a < b) << endl;
    cout << "(a == b): " << (a == b) << endl;
    cout << "(a != b): " << (a != b) << endl;
    cout << "(a >= b): " << (a >= b) << endl;
    cout << "(a <= b): " << (a <= b) << endl;

    return 0;
}