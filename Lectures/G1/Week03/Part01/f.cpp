#include <iostream>

using namespace std;

int main() {

    // Using char with arithmetic operators 

    // ASCII of 'A' is 65
    // so that is what used in the following calculations

    cout << 'A' + 1 << endl;
    cout << 'A' + 2 << endl;

    // ASCII of '0' is 48

    cout << '0' + '0' << endl;

    cout << 'A' / 2 << endl;

    cout << 'A' * 2 << endl;

    cout << 'A' % 10 << endl;

    return 0;
}